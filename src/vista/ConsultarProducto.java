    private void txtDatosBusquedaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDatosBusquedaKeyReleased
        switch(comboFiltro.getSelectedIndex()){
            case 0:
            try {
                table.setModel(producto.consultarProductoTotales());
            }catch (Exception e) {}
            break;
            case 1:
            try {
                producto.setIdProducto(Integer.parseInt(txtDatosBusqueda.getText()));
                table.setModel(producto.consultarProductoId());
            }catch (Exception e) {}
            break;
            case 2:
            try {
                producto.setNombre(txtDatosBusqueda.getText());
                table.setModel(producto.consultarProductoNombre());
            }catch (Exception e) {}
            break;
            case 3:
            try {
                producto.setMedida(txtDatosBusqueda.getText());
                table.setModel(producto.consultarProductoMedida());
            }catch (Exception e){}
            break;
            case 4:
            try {
                producto.setPresentacion(txtDatosBusqueda.getText());
                table.setModel(producto.consultarProductoPresentacion());
            }catch (Exception e){}
            break;
            case 5:
            try {
                producto.setPrecioSugerido(Double.parseDouble(txtDatosBusqueda.getText()));
                table.setModel(producto.consultarProductoPrecio());
            }catch(Exception e) {}
            break;
            case 6:
            try {
                producto.setDescripcion(txtDatosBusqueda.getText());
                table.setModel(producto.consultarProductoDescripcion());
            }catch (Exception e) {}
            break;
            default:
            break;
        }
    }//GEN-LAST:event_txtDatosBusquedaKeyReleased

